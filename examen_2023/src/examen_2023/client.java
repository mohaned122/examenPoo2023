package examen_2023;

import java.util.Objects;

/**
 *
 * @author user
 */
public class client {
    //nom, pr√©nom et adresse
    private String nom;
    private String prenom;
    private String adress;

    public client(String nom, String prenom, String adress) {
        this.nom = nom;
        this.prenom = prenom;
        this.adress = adress;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 37 * hash + Objects.hashCode(this.nom);
        hash = 37 * hash + Objects.hashCode(this.prenom);
        hash = 37 * hash + Objects.hashCode(this.adress);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final client other = (client) obj;
        if (!Objects.equals(this.nom, other.nom)) {
            return false;
        }
        if (!Objects.equals(this.prenom, other.prenom)) {
            return false;
        }
        return Objects.equals(this.adress, other.adress);
    }

    @Override
    public String toString() {
        return "client{" + "nom=" + nom + ", prenom=" + prenom + ", adress=" + adress + '}';
    }
    
    
}
